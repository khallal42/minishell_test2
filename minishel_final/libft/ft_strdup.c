#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*p;
	int		lens;

	i = 0;
	lens = ft_strlen(s1);
	p = malloc(lens + 1);
	if (p == 0)
		return (NULL);
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	p[lens] = '\0';
	return (p);
}
